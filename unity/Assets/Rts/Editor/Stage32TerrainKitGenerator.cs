        private static GameObject CreatePiecePrefab(PieceSpec spec, MaterialSet mats)
        {
            var root = new GameObject(spec.Id);
            var tag = root.AddComponent<Stage32TerrainPieceTag>();
            tag.terrainId = spec.Id;
            tag.displayName = spec.DisplayName;
            tag.category = spec.Category;
            tag.fineGridSize = spec.FineSize;
            tag.blocksMovement = spec.Blocks;
            tag.buildable = spec.Buildable;
            tag.road = spec.Road;
            tag.water = spec.Water;
            tag.resourceField = spec.Resource;
            tag.decorativeOnly = spec.Decorative;
            tag.notes = spec.Notes;
            tag.estimatedMaterialCount = 2;

            var baseMat = ResolveMaterial(spec, mats);
            AddCube(root, "base_beveled_slab", Vector3.zero, new Vector3(spec.FineSize.x * .5f, 0.08f, spec.FineSize.y * .5f), baseMat);
            AddCube(root, "dark_under_bevel", new Vector3(0, -0.055f, 0), new Vector3(spec.FineSize.x * .51f, 0.04f, spec.FineSize.y * .51f), mats.Rock);
            spec.DetailBuilder?.Invoke(root, mats);
            tag.estimatedMeshObjectCount = Math.Max(2, root.GetComponentsInChildren<MeshRenderer>().Length);
            tag.estimatedMaterialCount = CountMaterials(root);
            AddLodGroup(root);
            return root;
        }

        private static int CountMaterials(GameObject root)
        {
            var set = new HashSet<Material>();
            foreach (var r in root.GetComponentsInChildren<MeshRenderer>()) foreach (var m in r.sharedMaterials) if (m) set.Add(m);
            return set.Count;
        }

        private static Material ResolveMaterial(PieceSpec spec, MaterialSet m)
        {
            switch (spec.Category)
            {
                case Stage32TerrainCategory.Road: return m.Road;
                case Stage32TerrainCategory.BaseConstruction: return m.Concrete;
                case Stage32TerrainCategory.Obstacle: return m.Rock;
                case Stage32TerrainCategory.Resource: return m.Dirt;
                case Stage32TerrainCategory.Water: return m.Water;
                case Stage32TerrainCategory.BattlefieldProp: return m.Dirt;
                case Stage32TerrainCategory.Vegetation: return m.Grass;
                default: return ColorDistance(spec.Base, new Color(.25f,.36f,.18f)) < .18f ? m.Grass : m.Dirt;
            }
        }

        private static float ColorDistance(Color a, Color b) => Mathf.Abs(a.r-b.r)+Mathf.Abs(a.g-b.g)+Mathf.Abs(a.b-b.b);

        private static GameObject AddCube(GameObject parent, string name, Vector3 pos, Vector3 scale, Material mat)
        {
            var go = GameObject.CreatePrimitive(PrimitiveType.Cube);
            go.name = name;
            go.transform.SetParent(parent.transform, false);
            go.transform.localPosition = pos;
            go.transform.localScale = scale;
            var renderer = go.GetComponent<MeshRenderer>();
            renderer.sharedMaterial = mat;
            UnityEngine.Object.DestroyImmediate(go.GetComponent<Collider>());
            return go;
        }

