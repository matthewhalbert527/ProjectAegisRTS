        public static void GenerateTerrainKitBatch()
        {
            EnsureFolders();
            var materials = CreateMaterials();
            var specs = BuildSpecs();
            var report = new List<string>
            {
                "# Stage 32 Generated Terrain Kit Report",
                string.Empty,
                "Generated by Stage32TerrainKitGenerator from deterministic piece specs.",
                string.Empty,
                "This package creates high-quality first-pass terrain proxy prefabs using Unity primitives and shared materials. They are intended as realistic, Quest-safe terrain replacements until final artist-authored meshes/textures arrive.",
                string.Empty,
                "## Generated Pieces",
                string.Empty,
                "| ID | Category | Fine Size | Blocks | Buildable | Notes |",
                "|---|---|---:|---:|---:|---|"
            };

            foreach (var spec in specs)
            {
                var prefab = CreatePiecePrefab(spec, materials);
                var path = $"{PrefabFolder}/{spec.Id}.prefab";
                PrefabUtility.SaveAsPrefabAsset(prefab, path);
                UnityEngine.Object.DestroyImmediate(prefab);
                report.Add($"| `{spec.Id}` | {spec.Category} | {spec.FineSize.x}x{spec.FineSize.y} | {spec.Blocks} | {spec.Buildable} | {spec.Notes?.Replace("|", "/")} |");
            }

            CreateReviewScene(specs);
            var reportPath = GetRepoDocsPath(ReportFileName);
            Directory.CreateDirectory(Path.GetDirectoryName(reportPath));
            File.WriteAllLines(reportPath, report);
            AssetDatabase.SaveAssets();
            AssetDatabase.Refresh();
            Debug.Log($"Stage 32 terrain kit generated {specs.Count} prefabs. Scene: {ScenePath}. Report: docs/{ReportFileName}");
        }

        private static void EnsureFolders()
        {
            CreateFolderRecursive(MaterialFolder);
            CreateFolderRecursive(PrefabFolder);
            CreateFolderRecursive(SceneFolder);
            Directory.CreateDirectory(GetRepoDocsPath(string.Empty));
        }

        private static string GetRepoDocsPath(string fileName)
        {
            var unityRoot = Directory.GetParent(Application.dataPath);
            var repoRoot = unityRoot?.Parent;
            if (repoRoot == null)
                return Path.Combine("docs", fileName);
            return Path.Combine(repoRoot.FullName, "docs", fileName);
        }

        private static void CreateFolderRecursive(string path)
        {
            var parts = path.Split('/');
            var current = parts[0];
            for (var i = 1; i < parts.Length; i++)
            {
                var next = current + "/" + parts[i];
                if (!AssetDatabase.IsValidFolder(next))
                    AssetDatabase.CreateFolder(current, parts[i]);
                current = next;
            }
        }

