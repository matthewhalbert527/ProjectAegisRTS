        private static Vector3 RandPos(int i, float radius, float y)
        {
            var x = Mathf.Sin(i * 12.9898f) * 43758.5453f;
            var z = Mathf.Sin(i * 78.233f) * 12345.6789f;
            x = (x - Mathf.Floor(x)) * 2f - 1f;
            z = (z - Mathf.Floor(z)) * 2f - 1f;
            return new Vector3(x * radius, y, z * radius);
        }

        private static Vector3 Circle(int i, int total, float radius, float y)
        {
            var a = i * Mathf.PI * 2f / total;
            return new Vector3(Mathf.Cos(a) * radius, y, Mathf.Sin(a) * radius);
        }

        private static void CreateReviewScene(List<PieceSpec> specs)
        {
            var scene = EditorSceneManager.NewScene(NewSceneSetup.EmptyScene, NewSceneMode.Single);
            var camObj = new GameObject("Main Camera");
            var cam = camObj.AddComponent<Camera>();
            cam.orthographic = true;
            cam.orthographicSize = 9f;
            cam.transform.position = new Vector3(8f, 12f, -10f);
            cam.transform.rotation = Quaternion.Euler(60f, 0f, 0f);
            cam.clearFlags = CameraClearFlags.SolidColor;
            cam.backgroundColor = new Color(.08f,.09f,.10f);
            camObj.tag = "MainCamera";

            var light = new GameObject("Directional Light").AddComponent<Light>();
            light.type = LightType.Directional;
            light.intensity = 1.1f;
            light.transform.rotation = Quaternion.Euler(50f, -35f, 0f);

            var root = new GameObject("Stage32TerrainReviewRoot");
            var columns = 8;
            for (var i = 0; i < specs.Count; i++)
            {
                var spec = specs[i];
                var prefab = AssetDatabase.LoadAssetAtPath<GameObject>($"{PrefabFolder}/{spec.Id}.prefab");
                if (!prefab) continue;
                var instance = (GameObject)PrefabUtility.InstantiatePrefab(prefab);
                instance.transform.SetParent(root.transform, false);
                var x = (i % columns) * 1.55f;
                var z = -(i / columns) * 1.55f;
                instance.transform.position = new Vector3(x, 0, z);

                var label = new GameObject($"label_{spec.Id}");
                label.transform.SetParent(root.transform, false);
                label.transform.position = new Vector3(x - .55f, .22f, z - .72f);
                var tm = label.AddComponent<TextMesh>();
                tm.text = spec.Id;
                tm.characterSize = .075f;
                tm.anchor = TextAnchor.MiddleLeft;
                tm.color = Color.white;
                label.transform.rotation = Quaternion.Euler(60f, 0f, 0f);
            }
            root.transform.position = new Vector3(-5.6f, 0, 4.5f);
            EditorSceneManager.SaveScene(scene, ScenePath);
        }
